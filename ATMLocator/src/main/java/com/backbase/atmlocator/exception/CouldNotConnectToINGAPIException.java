package com.backbase.atmlocator.exception;

public class CouldNotConnectToINGAPIException extends RuntimeException {

	
	private static final long serialVersionUID = -3507377636380287834L;

	public CouldNotConnectToINGAPIException() {
		super();
	}

	public CouldNotConnectToINGAPIException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public CouldNotConnectToINGAPIException(String message, Throwable cause) {
		super(message, cause);
	}

	public CouldNotConnectToINGAPIException(String message) {
		super(message);
	}

	public CouldNotConnectToINGAPIException(Throwable cause) {
		super(cause);
	}

	
	
	
	
}
